#include "textflag.h"


// adagradStepSSE2 — SSE2 only.
TEXT ·adagradStepSSE2(SB), NOSPLIT, $0-120
	MOVQ out+0(FP), AX
	MOVQ G+24(FP), R8
	MOVQ params+48(FP), R9
	MOVQ grads+72(FP), R10
	MOVQ out_len+8(FP), CX

	MOVSD lr+96(FP), X8
	SHUFPD $0, X8, X8
	MOVSD eps+104(FP), X9
	SHUFPD $0, X9, X9
	MOVSD wd+112(FP), X10
	SHUFPD $0, X10, X10

	CMPQ CX, $2
	JL   ag_sse2_tail
ag_sse2_loop:
	MOVUPD (R8), X0
	MOVUPD (R9), X1
	MOVUPD (R10), X2

	MOVAPD X2, X3
	MOVAPD X1, X11
	MULPD  X10, X11
	ADDPD  X11, X3

	MOVAPD X3, X11
	MULPD  X11, X11
	ADDPD  X11, X0
	MOVUPD X0, (R8)

	MOVAPD X0, X4
	SQRTPD X4, X4
	ADDPD  X9, X4
	MOVAPD X3, X5
	DIVPD  X4, X5
	MULPD  X8, X5
	SUBPD  X5, X1
	MOVUPD X1, (AX)

	ADDQ $16, AX
	ADDQ $16, R8
	ADDQ $16, R9
	ADDQ $16, R10
	SUBQ $2, CX
	CMPQ CX, $2
	JGE  ag_sse2_loop

ag_sse2_tail:
	CMPQ CX, $0
	JLE  ag_sse2_done
	MOVSD (R8), X0
	MOVSD (R9), X1
	MOVSD (R10), X2

	MOVAPD X2, X3
	MOVAPD X1, X11
	MULSD  X10, X11
	ADDSD  X11, X3

	MOVAPD X3, X11
	MULSD  X11, X11
	ADDSD  X11, X0
	MOVSD  X0, (R8)

	MOVAPD X0, X4
	SQRTSD X4, X4
	ADDSD  X9, X4
	MOVAPD X3, X5
	DIVSD  X4, X5
	MULSD  X8, X5
	SUBSD  X5, X1
	MOVSD  X1, (AX)

ag_sse2_done:
	RET
